public class Operators {
    public static void main(String[] args) {
        // 1.ArithmsticOperators
        int a = 4;
        int b = 5-a;
        int c = 5*a;
        int d = 6%a;
        // 2. Assignment Operators
        d *=3;
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        // 3. Comparrision Operators
        System.out.println(55>=64);
        System.out.println(64==56);
        // 4. Logical operators
        System.out.println(64>4 && 64>88);
        System.out.println(64>55 || 64>98);

    }
    
}
